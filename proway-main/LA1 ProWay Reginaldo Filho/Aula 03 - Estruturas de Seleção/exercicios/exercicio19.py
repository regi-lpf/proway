n = int(input("Insira o numero do dia(1-7): "))

if n == 1:
    print("Domingo")
elif n == 2:
    print("Segunda")
elif n == 3:
    print("Terça")
elif n == 4:
    print("Quarta")
elif n == 5:
    print("Quinta")
elif n == 6:
    print("Sexta")
elif n == 7:
    print("Sábado")
else:
    print("Inicie o programa novamente e insira um número de 1 a 7.")